package Array_program;

import java.util.Arrays;

public class MultidimensionArr {

	public static void main(String[] args) {

      String[][] name= {{"kittu","mohini","ashu"},{"jal","pune","nasik"}};
     

      for(int i=0;i<=1;i++)
      {
    	  for(int j=0;j<=2;j++)
    	  {
    		  System.out.println(name[i][j]);
    	  }
      }
      
	}

}
